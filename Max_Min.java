public class Max_Min {
    public static void main(String[] args) {
        System.out.print("Min of byte " + Byte.MIN_VALUE);
        System.out.println(" Max of byte " + Byte.MAX_VALUE);
        System.out.print("Min of int " + Integer.MIN_VALUE);
        System.out.println(" Max of int " + Integer.MAX_VALUE);
        System.out.print("Min of long " + Long.MIN_VALUE);
        System.out.println(" Max of long " + Long.MAX_VALUE);
        System.out.print("Min of short " + Short.MIN_VALUE);
        System.out.println(" Max of short " + Short.MAX_VALUE);
        System.out.print("Min of float " + Float.MIN_VALUE);
        System.out.println(" Max of float " + Float.MAX_VALUE);
        System.out.print("Min of double " + Double.MIN_VALUE);
        System.out.println(" Max of double " + Double.MAX_VALUE);
        // char  Don't have min or max.
    }
}
